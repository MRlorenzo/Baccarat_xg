async function write(port , data){
  return new Promise((resolve,reject)=>{
    port.write(data, function(err) {
      if (err) {
        reject(err);
      }else{
        //console.log('success...', data)
        resolve()
      }
    })
  })
}
export async function runDebug( comName = 'COM2') {
	if (process.env.NODE_ENV !== 'development'){
	    throw new Error('生产环境下禁止');
    }

	const options = {
		baudRate: 4800
	};

	const port = new SerialPort(comName , options, function (err) {
		if (err) {
			return console.log('Error: ', err.message)
		}
	});
	for (const buf of dataList){
		try {
			await write(port , buf);
		} catch (error) {
			console.error(error)
		}
	}
}